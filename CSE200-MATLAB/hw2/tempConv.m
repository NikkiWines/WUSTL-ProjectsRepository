function temperatures=tempConv(celcius)

%This function takes in a series of temperature values in degrees Ceclius
%and converts them to fahrenheit, kelvin and rankine
%INPUTS: 
%celcius-- vector input 
%OUTPUTS: 
%fahrenheit-- vector output  as a function of celcius
%kelvin-- vector output  as a function of celcius
%rankine-- vector output  as a function of celcius

fahrenheit= (celcius *1.8)+32;
kelvin= celcius +237.15;
rankine= (celcius*1.8) +32 + 459.67;

temperatures= vertcat(fahrenheit, kelvin, rankine);

